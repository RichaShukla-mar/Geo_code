select B.GDA_DFND_AREA_CHLD_ROWID as bdaLocRowID, C.NAME as name, D.NAME as type,
CASE WHEN C.ACTIVE_FLAG ='Y' THEN 'Active'
     WHEN C.ACTIVE_FLAG='N' THEN 'InActive'
     ELSE C.ACTIVE_FLAG end  as status
from C_GDA_DFND_AREA A
INNER JOIN C_GDA_DFND_AREA_REL B ON B.GDA_DFND_AREA_PRNT_ROWID = A.ROWID_OBJECT AND B.HUB_STATE_IND = 1
INNER JOIN C_GDA_DFND_AREA C ON C.ROWID_OBJECT = B.GDA_DFND_AREA_CHLD_ROWID AND C.HUB_STATE_IND = 1
INNER JOIN C_TYP_TYPE D ON D.CODE = C.TYP_TYPE_CD AND D.HUB_STATE_IND = 1
